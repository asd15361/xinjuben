/**
 * src/main/application/script-generation/prompt/first-draft-system-prompt.ts
 *
 * 首稿 System Prompt：只保留绝对红线。
 *
 * 从 create-script-generation-prompt.ts 中提取的不可违反核心规则，
 * 作为 AI 调用的 systemInstruction 传入，与 user prompt 分离。
 * 这样做可以避免长 prompt 中核心规则被上下文淹没。
 */

export function buildFirstDraftSystemPrompt(): string {
  return [
    '【短剧绝对红线 · 不可违反】',
    '1. 极限密度：每集施压→反击→留钩子。第1集开局30秒必须有巨大危机，1分钟必须有反转。',
    '2. 主角风骨：可以忍，但不能怕；可以装弱，但不能真弱；受辱同一集必须讨回。',
    '3. 反派高智：用规则杀人、用权势压人、用利益分化，微笑着逼死。禁止无脑吼叫。',
    '4. 禁止真窝囊行为：下跪/求饶/认错（非假意配合）、被打后只愣住不反击、空头威胁、连续道歉退让。',
    '5. 禁止画外音/旁白/OS/心理总结。',
    '6. 每场必须有结果落地：人被拖走/证据易手/门被撞开/伤口见血/身份暴露/站队翻转。',
    '7. 相邻两场推进手法必须变化。',
    '8. 师父/长老/高手不能替主角收账。',
    '9. 公审/议事最多连续1集占主场。',
    '10. 最后3集优先回收人账、证据账、规则账、关系账。',
    '11. 爽感三件套（当 catharsisMoment 存在时必须执行）：动态Punchline→反派溃败表现→旁观者锚点。',
    '12. 格式：第X集标题独占一行；场号格式 X-Y 时段；每场：人物表→△动作→角色名：对白。禁用旧三段标签。'
  ].join('\n')
}
