/**
 * Type guard: returns true if running in renderer process.
 * Used to enforce renderer-only constraints at runtime.
 *
 * NOTE: This is a best-effort check. The constitutional rules
 * must be enforced by code structure, not just runtime checks.
 */
export function isRendererProcess(): boolean {
  // In Electron, renderer process has access to window.api but not direct Node
  // This is a structural check, not a security guarantee
  return (
    typeof window !== 'undefined' &&
    typeof (window as unknown as { api?: unknown }).api !== 'undefined'
  )
}

/**
 * Assert that code is running in renderer process.
 * Throws if called in main process (which would indicate a code smell).
 */
export function assertRendererProcess(): void {
  if (!isRendererProcess()) {
    throw new Error(
      '[AuthorityConstitution] CODE SMELL: Renderer-only constraint called outside renderer process'
    )
  }
}

/**
 * Assert that a fact is being read (not written) by renderer.
 * This is a documentation assertion — actual enforcement happens at IPC boundary.
 */
export function assertRendererReadOnly(fact: AuthorityOwnedFact): void {
  if (!RendererReadOnlyFacts.includes(fact)) {
    throw new Error(
      `[AuthorityConstitution] "${fact}" is not in RendererReadOnlyFacts — review authority constitution`
    )
  }
}
