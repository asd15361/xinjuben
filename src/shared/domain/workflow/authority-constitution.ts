/**
 * Types of authority failure that require explicit error surfacing.
 * Each failure type has a specific contract for how it must be handled.
 */
export const AuthorityFailureType = {
  /**
   * IPC call to main process failed entirely
   * Contract: Surface as error, do not retry silently, do not use cached state
   */
  IPC_FAILURE: 'ipc_failure',

  /**
   * Project does not exist or cannot be loaded
   * Contract: Surface as error with PROJECT_NOT_FOUND code, no optimistic fallback
   */
  PROJECT_MISSING: 'project_missing',

  /**
   * Authority result is null/undefined
   * Contract: Surface as error, do not assume "no content" = "empty but valid"
   */
  AUTHORITY_RESULT_NULL: 'authority_result_null',

  /**
   * Authority result is missing required fields
   * Contract: Surface as error with INCOMPLETE_RESULT code, do not fill defaults
   */
  INCOMPLETE_RESULT: 'incomplete_result',

  /**
   * Result is stale (timestamp too old)
   * Contract: Surface as warning if displayable, error if blocking
   */
  STALE_RESULT: 'stale_result',

  /**
   * Main process threw an exception
   * Contract: Surface the exception as error, do not catch and continue
   */
  MAIN_EXCEPTION: 'main_exception',

  /**
   * Orchestrator bypass detected — live path not followed
   * Contract: Surface as error, do not allow旁路 to become defacto
   */
  ORCHESTRATOR_BYPASS: 'orchestrator_bypass'
} as const

export type AuthorityFailureTypeValue =
  (typeof AuthorityFailureType)[keyof typeof AuthorityFailureType]

/**
 * Authority failure error — thrown when authority check fails.
 * This is the explicit error surface for authority violations.
 */
export class AuthorityFailureError extends Error {
  readonly failureType: AuthorityFailureTypeValue
  readonly fact: AuthorityOwnedFact
  readonly context: string
  readonly originalError?: unknown

  constructor(
    failureType: AuthorityFailureTypeValue,
    fact: AuthorityOwnedFact,
    context: string,
    originalError?: unknown
  ) {
    const errorCode = `AUTHORITY_FAILURE_${failureType.toUpperCase()}`
    const message = `[${errorCode}] Authority failure for "${fact}": ${context}`
    super(message)
    this.name = 'AuthorityFailureError'
    this.failureType = failureType
    this.fact = fact
    this.context = context
    this.originalError = originalError
  }
}

/**
 * Result of an authority check — either success or explicit failure.
 * Used to enforce "no silent fallback" at IPC boundaries.
 */
export type AuthorityCheckResult<T> =
  | { ok: true; value: T }
  | { ok: false; failure: AuthorityFailureError }

/**
 * Create a successful authority check result.
 */
export function authorityOk<T>(value: T): AuthorityCheckResult<T> {
  return { ok: true, value }
}

/**
 * Create a failed authority check result.
 */
export function authorityFail(
  failureType: AuthorityFailureTypeValue,
  fact: AuthorityOwnedFact,
  context: string,
  originalError?: unknown
): AuthorityCheckResult<never> {
  return {
    ok: false,
    failure: new AuthorityFailureError(failureType, fact, context, originalError)
  }
}

/**
 * Assert that an authority check passed, throwing if it failed.
 * Use at IPC boundaries to enforce explicit error surfacing.
 */
export function assertAuthorityCheck<T>(result: AuthorityCheckResult<T>, context = ''): T {
  if (!result.ok) {
    if (context) {
      result.failure.message = `[${context}] ${result.failure.message}`
    }
    throw result.failure
  }
  return result.value
}

// =============================================================================
// AUTHORITY VALIDATION HELPERS
// =============================================================================

/**
 * Check if a fact is authority-owned (requires MAIN process authority).
 */
export function isAuthorityOwnedFact(fact: string): fact is AuthorityOwnedFact {
  return Object.values(AuthorityOwnedFacts).includes(fact as AuthorityOwnedFact)
}

/**
 * Validate that a value is not null/undefined.
 * Returns authority failure result if null/undefined.
 */
export function requireAuthorityValue<T>(
  value: T | null | undefined,
  fact: AuthorityOwnedFact,
  context: string
): AuthorityCheckResult<T> {
  if (value === null || value === undefined) {
    return authorityFail(AuthorityFailureType.AUTHORITY_RESULT_NULL, fact, context)
  }
  return authorityOk(value)
}

/**
 * Validate that an object has all required fields.
 * Returns authority failure result if any field is missing.
 */
export function requireAuthorityFields<T extends object>(
  obj: Partial<T> | null | undefined,
  fact: AuthorityOwnedFact,
  context: string,
  requiredFields: (keyof T)[]
): AuthorityCheckResult<T> {
  if (obj === null || obj === undefined) {
    return authorityFail(AuthorityFailureType.AUTHORITY_RESULT_NULL, fact, context)
  }

  const missingFields = requiredFields.filter((field) => !(field in obj))
  if (missingFields.length > 0) {
    return authorityFail(
      AuthorityFailureType.INCOMPLETE_RESULT,
      fact,
      `${context}: missing fields [${missingFields.join(', ')}]`
    )
  }

  return authorityOk(obj as T)
}

/**
 * Validate that an IPC result is not a forbidden fallback pattern.
 * Throws AuthorityFailureError if a forbidden pattern is detected.
 */
export function enforceNoForbiddenFallback(
  pattern: ForbiddenFallbackPattern,
  context: string,
  originalError?: unknown
): void {
  const fact = detectForbiddenFallbackFact(pattern)
  throw new AuthorityFailureError(
    AuthorityFailureType.IPC_FAILURE,
    fact,
    `Forbidden fallback pattern detected: ${pattern} — ${context}`,
    originalError
  )
}

/**
 * Detect which authority-owned fact is at risk from a forbidden fallback pattern.
 * Used for error reporting when a forbidden pattern is detected.
 */
function detectForbiddenFallbackFact(pattern: ForbiddenFallbackPattern): AuthorityOwnedFact {
  switch (pattern) {
    case ForbiddenFallbackPatterns.CATCH_SET_STAGE:
    case ForbiddenFallbackPatterns.UNKNOWN_AUTHORITY_DEFAULT_CHAT:
      return AuthorityOwnedFacts.STAGE
    case ForbiddenFallbackPatterns.MISSING_RESULT_OPTIMISTIC_STAGE:
      return AuthorityOwnedFacts.STAGE
    case ForbiddenFallbackPatterns.RENDERER_COMPUTING_DERIVED_FACTS:
      return AuthorityOwnedFacts.RESUME_ELIGIBILITY
    case ForbiddenFallbackPatterns.STALE_CACHED_STATE_FALLBACK:
      return AuthorityOwnedFacts.SCRIPT_RUNTIME_STATE
    default:
      return AuthorityOwnedFacts.GENERATION_STATUS
  }
}

