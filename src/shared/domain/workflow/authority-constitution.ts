/**
 * authority-constitution.ts — Hard Rules for Authority Failure Behavior
 *
 * PURPOSE:
 * This module defines the CONSTITUTION for what happens when authority is absent.
 * It is the SINGLE SOURCE for "no authority = explicit error, no state progression."
 *
 * PRINCIPLES:
 * - Authority-owned facts: ONLY main process may produce these
 * - Forbidden fallback patterns: Renderer MUST NOT silently fall back when authority fails
 * - Authority failure contract: All authority failures surface as explicit errors
 *
 * ANTI-PATTERNS THIS CONSTITUTION BANS:
 * 1. `catch { setStage(...) }` — renderer catching errors and setting stage locally
 * 2. `if (!result.project) { setStage(...) }` — optimistic local stage updates
 * 3. `?? existingState` — using stale cached state when fresh authority state unavailable
 * 4. Silent IPC failure with local fallback — not surfacing main process errors
 * 5. Incomplete result assumption — treating partial results as complete
 *
 * RENDERER RULE (fundamental):
 * - Renderer is ONLY a consumer, NEVER a producer for core truths
 * - When authority is unavailable, renderer MUST surface the error, NOT substitute
 *
 * This file is the CONSTITUTION — it defines the rules, not the implementation.
 * Actual enforcement happens at IPC boundaries and orchestrator layer.
 */

// =============================================================================
// AUTHORITY-OWNED FACTS
// =============================================================================

/**
 * Facts that require MAIN process authority.
 *
 * These facts MUST only be produced by main process.
 * Any attempt to produce these in renderer must fail explicitly.
 *
 * RENDERER CONSUMER NOTE:
 * Renderer may READ these facts via IPC, but must NEVER:
 * - Write them to local state as if they were authoritative
 * - Use them to compute other "derived" facts locally
 * - Cache them and use cached version when IPC fails
 */
export const AuthorityOwnedFacts = {
  /**
   * Current workflow stage — derived by main from project snapshot
   * NEVER: renderer computing stage locally from partial state
   */
  STAGE: 'stage',

  /**
   * Blocked reason — computed by main's contract policy
   * NEVER: renderer inferring blocked reason from UI state
   */
  BLOCKED_REASON: 'blockedReason',

  /**
   * Resume eligibility — derived by main from progress board
   * NEVER: renderer deciding "canResume" from cached board state
   */
  RESUME_ELIGIBILITY: 'resumeEligibility',

  /**
   * Generation status — owned by main, represents in-flight generation
   * NEVER: renderer setting generationStatus locally in catch block
   */
  GENERATION_STATUS: 'generationStatus',

  /**
   * Script runtime state — built up by main's script generation
   * NEVER: renderer modifying scriptDraft/scriptProgressBoard locally
   */
  SCRIPT_RUNTIME_STATE: 'scriptRuntimeState',

  /**
   * Script progress board — maintained by main's batch observer
   * NEVER: renderer writing to progressBoard directly
   */
  PROGRESS_BOARD: 'progressBoard',

  /**
   * Failure resolution — created by main on generation failure
   * NEVER: renderer constructing failureResolution locally
   */
  FAILURE_RESOLUTION: 'failureResolution',

  /**
   * Failure history — accumulated by main across generations
   * NEVER: renderer managing failureHistory independently
   */
  FAILURE_HISTORY: 'failureHistory',

  /**
   * Formal facts — confirmed by main's formal fact handlers
   * NEVER: renderer declaring formal facts locally
   */
  FORMAL_FACTS: 'formalFacts',

  /**
   * Detailed outline blocks — generated by main
   * NEVER: renderer computing or modifying detailedOutlineBlocks locally
   */
  DETAILED_OUTLINE_BLOCKS: 'detailedOutlineBlocks',

  /**
   * Character drafts — generated by main
   * NEVER: renderer writing characterDrafts locally
   */
  CHARACTER_DRAFTS: 'characterDrafts',

  /**
   * Outline draft — generated by main
   * NEVER: renderer writing outlineDraft locally
   */
  OUTLINE_DRAFT: 'outlineDraft'
} as const

export type AuthorityOwnedFact = (typeof AuthorityOwnedFacts)[keyof typeof AuthorityOwnedFacts]

/**
 * Facts that renderer may read but must never produce or cache as authoritative.
 * This is a subset of AuthorityOwnedFacts focused on what renderer commonly accesses.
 */
export const RendererReadOnlyFacts: ReadonlyArray<AuthorityOwnedFact> = [
  AuthorityOwnedFacts.STAGE,
  AuthorityOwnedFacts.BLOCKED_REASON,
  AuthorityOwnedFacts.RESUME_ELIGIBILITY,
  AuthorityOwnedFacts.GENERATION_STATUS,
  AuthorityOwnedFacts.SCRIPT_RUNTIME_STATE,
  AuthorityOwnedFacts.PROGRESS_BOARD,
  AuthorityOwnedFacts.FAILURE_RESOLUTION,
  AuthorityOwnedFacts.FAILURE_HISTORY,
  AuthorityOwnedFacts.FORMAL_FACTS,
  AuthorityOwnedFacts.DETAILED_OUTLINE_BLOCKS,
  AuthorityOwnedFacts.CHARACTER_DRAFTS,
  AuthorityOwnedFacts.OUTLINE_DRAFT
] as const

// =============================================================================
// FORBIDDEN FALLBACK PATTERNS
// =============================================================================

/**
 * Fallback patterns that are FORBIDDEN when authority is unavailable.
 *
 * These patterns represent "silent substitution" —renderer substituting
 * its own judgment when authority fails. This is a constitutional violation.
 *
 * CONSTITUTIONAL RULE:
 * When any of these patterns would be triggered, instead:
 * 1. Surface the authority failure as an explicit error
 * 2. Do NOT progress business state
 * 3. Let the error bubble up to error boundary
 */
export const ForbiddenFallbackPatterns = {
  /**
   * FORBIDDEN: catch { setStage(...) }
   * Anti-pattern: renderer catches error and sets stage locally as fallback
   * Constitutional rule: errors must surface, not silently handle
   */
  CATCH_SET_STAGE: 'catch-setStage',

  /**
   * FORBIDDEN: if (!result.project) { setStage(...) }
   * Anti-pattern: renderer checks for result absence and updates stage optimistically
   * Constitutional rule: missing project is authority failure, must surface
   */
  MISSING_RESULT_OPTIMISTIC_STAGE: 'missing-result-optimistic-stage',

  /**
   * FORBIDDEN: ?? existingCachedState
   * Anti-pattern: using stale cached state when fresh authority state unavailable
   * Constitutional rule: stale state is not authority, must surface
   */
  STALE_CACHED_STATE_FALLBACK: 'stale-cached-state-fallback',

  /**
   * FORBIDDEN: Silently swallowing IPC errors and continuing
   * Anti-pattern: IPC call fails but renderer continues as if success
   * Constitutional rule: IPC failure is authority failure, must surface
   */
  SILENT_IPC_FAILURE: 'silent-ipc-failure',

  /**
   * FORBIDDEN: Treating incomplete result as complete
   * Anti-pattern: result has missing fields but code proceeds as if complete
   * Constitutional rule: incomplete result is authority failure, must surface
   */
  INCOMPLETE_RESULT_AS_COMPLETE: 'incomplete-result-as-complete',

  /**
   * FORBIDDEN: Fallback to "chat" stage when authority unclear
   * Anti-pattern: unclear authority state → default to chat
   * Constitutional rule: unclear authority must surface as error, not default
   */
  UNKNOWN_AUTHORITY_DEFAULT_CHAT: 'unknown-authority-default-chat',

  /**
   * FORBIDDEN: renderer computing derived facts from cached data
   * Anti-pattern: renderer computes "canResume" from cached board
   * Constitutional rule: derived facts require fresh authority computation
   */
  RENDERER_COMPUTING_DERIVED_FACTS: 'renderer-computing-derived-facts',

  /**
   * FORBIDDEN: Silent retry on IPC failure
   * Anti-pattern: IPC fails → retry silently → proceed if succeeds
   * Constitutional rule: IPC failure must surface, not silently retry
   */
  SILENT_IPC_RETRY: 'silent-ipc-retry'
} as const

export type ForbiddenFallbackPattern =
  (typeof ForbiddenFallbackPatterns)[keyof typeof ForbiddenFallbackPatterns]

