import type { OutlineDraftDto } from '../../../../shared/contracts/workflow'
import { buildFormalFactSceneDirectives } from './build-formal-fact-scene-directives'

function hasConfirmedFact(outline: OutlineDraftDto, label: string): boolean {
  return outline.facts.some((fact) => fact.status === 'confirmed' && fact.label === label)
}

export function buildEpisodeSceneDirectives(outline: OutlineDraftDto, episodeNo: number): string[] {
  const lines = [
    '每段都写成正在发生的戏，不要解释人物层次，不要总结主题。',
    '先把这场最能卖的反差、错位或关系不对劲打出来，再补其他信息。',
    '先让人物带着站位和关系进场，再交代信息；不要把对白写成播报现状。',
    '“站位”“钉子句”“关系施压”“说明”“总结”这些词只准留在你脑子里，不准写进 Action、Dialogue、Emotion 正文。',
    '正文里不准出现“（站位：…）”“（钉子句）”“（说明）”这类幕后注释。',
    '站位不能只写成态度，必须听得出这个人当前往哪边站、冲谁去、护谁、顶谁或躲谁。',
    '如果句子里没有关系选择和代价感，说明站位还没落地，继续改。',
    '对白里至少要有 1 句能单独摘出来当钉子句。',
    '对白必须能听出“这句话为什么非得这个人现在说”，不要写成谁都能说的功能句。',
    '如果一句对白换给对手、主角或旁人说都成立，说明这句还没写到人身上，必须继续改。',
    '对白不能只有立场没有压强；要听得出人物是在嘴硬、难堪、心虚、压着火，还是在硬逼别人。',
    '对白不要一口气把信息讲满，优先让人物先顶一句、躲一句、反咬一句，再把真正要命的信息漏出来。',
    '一句对白里不要同时背太多任务；宁可拆成两三句有停顿、有抢话、有被打断的人话，也不要写成整段说明。',
    '不要直接端出“他很生气”“她很难过”这种情绪结论，要把情绪压强落在停顿、顶嘴、反咬、让步、手上动作和代价里。',
    '如果这场有关系施压，先写难堪、对顶、嘴硬、退让这些当下反应，再补背景信息。',
    'Emotion 只写本场情绪变化和逼出的下一步，不写分析报告。',
    '每场都要尽快给出一口当下可兑现的情绪，不要一直拖到后面才让观众爽、痛或心里一紧。',
    '每一场都必须比上一场更往前一步，不能只把上一场换个说法重新写一遍。'
  ]

  if (episodeNo === 1) {
    lines.push('第 1 场先只盯住一条主压强：对手当面逼压，主角为了守人和守物硬压着不动，当场吃亏。')
    lines.push('第 1 场开头 30% 内容内就要起冲突，不能先铺世界观和人物分析。')
    lines.push('第 1 场必须写清主角眼下最想守住的人或物，而且这个选择要立刻害他付出看得见的代价。')
    lines.push('第 1 场必须让观众明确看见“他明明能动手，却因为旧规矩、旧誓言或师父交代硬压着不动”。')
    lines.push('第 1 场必须让观众一眼看懂这部戏最不对劲、最反常、最值得点开的地方，不准把设定成交点藏到后面。')
    lines.push('第 1 场对白先不要急着讲明白前情，先让观众从顶撞、嘴硬、试探、护短里听见关系和压强。')
    lines.push('第 1 场关键对白必须带人物自己的口气和站位，不能换个人说也通。')

    if (hasConfirmedFact(outline, '对手压力')) {
      lines.push('第 1 场必须让对手本人亲自出场并当面施压，不能只让手下代打，也不能只用“李爷”“那位爷”这类影子称呼带过。')
      lines.push('对手施压时，必须直接冲着主角要守的人或要守的东西来，让观众一眼看懂谁在压谁。')
    }

    if (hasConfirmedFact(outline, '师父角色')) {
      lines.push('第 1 场必须让师父的嘱托、规矩、旧事、物件或警告至少一种正在起作用，而且要直接影响主角当场的选择。')
    }

    if (hasConfirmedFact(outline, '关键关系')) {
      lines.push('第 1 场必须让关键关系对象当场被卷进冲突，并和主角发生一次带伤感的互动。')
    }

    if (hasConfirmedFact(outline, '关键道具')) {
      lines.push('第 1 场必须让关键道具进入动作或对白，而且它要成为本场冲突焦点之一。')
    }

    lines.push('第 1 场必须带出一次师父、旧事或旧规矩在主角心里被触发的瞬间，最好直接出现一句师父旧话、旧规矩或旧告诫，形成看得见的记忆回声。')
    lines.push('如果上游主冲突里已经有妖兽危机或更大外压，第 1 场至少要露一个不安信号，不能把整条大压强完全留空。')
    lines.push('第 1 场结尾必须留下立刻要发生的硬钩子，例如搜身、夺物、带走人、限时威胁。')
    lines.push('第 1 场结尾要让人明确感到“下一秒麻烦会更大”，不能只是正常收尾。')
    lines.push('第 1 场最后两句里，至少有一句必须是“下一秒就会发生”的具体动作或逼压，不能只停在主角心里发紧、决定变重这种感受上。')
    lines.push('如果结尾没有门外脚步、刀再压近、手已经伸过来、对手开始动手、有人被拖走、时限被钉死这类眼前动作，就说明钩子还没落地，继续改。')
    lines.push('第 1 场最后一句优先写“已经开始发生”的动作，不要只写“他准备去做什么”“他意识到什么”。')
    lines.push('如果结尾还是停在计划、决心、意识到、知道自己必须这类收口，说明承接点还是假的，必须改成已经压到眼前的动作或危险。')
    lines.push('只要上面这条主压强已经成立，其他信息一律让位，不要平均用力。')
  }

  return [...lines, ...buildFormalFactSceneDirectives(outline, episodeNo)]
}
